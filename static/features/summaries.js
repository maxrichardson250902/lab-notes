// ── SUMMARIES ─────────────────────────────────────────────────────────────────
async function renderSummaries(el){
  const data=await api('GET','/api/summaries');
  S.summaries=data.summaries||[];
  if(!S.summaries.length){
    el.innerHTML='<div class="empty"><big>&#128202;</big>No summaries yet — these are generated by the 3090 during nightly enrichment, or trigger enrichment manually from the todo app.</div>';
    return;
  }
  el.innerHTML=S.summaries.map(s=>{
    const steps=JSON.parse(s.next_steps||'[]');
    return '<div class="summary-card">'+
      '<div class="summary-group">'+esc(s.group_name)+'</div>'+
      '<div class="summary-text">'+esc(s.summary)+'</div>'+
      (steps.length?'<div class="next-steps">'+steps.map(step=>'<div class="next-step">'+esc(step)+'</div>').join('')+'</div>':'')+
      '<div class="summary-updated">Updated '+relTime(s.updated)+'</div>'+
    '</div>';
  }).join('');
}
registerView('summaries', renderSummaries);
